nome = input('Digite seu nome completo ')
print('Seu nome em Maiúscula é: {}'.format(nome.upper()))
print('Seu nome em minuscula é: {}'.format(nome.lower()))
print('Seu nome possui "{}" letras'.format(len(nome)))
print('Seu nome com as iniciais em Maiúscula: {}'.format(nome.title()))
print('Seu nome separado: {} '.format(nome.split()))
print('Seu primeiro nome possui "{}" letras'.format(nome.find(' ')))


